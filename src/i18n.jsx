const LangContext = createContext();

export function LangProvider({ children }) {
  const [lang, setLang] = useState(() => {
    try { return localStorage.getItem('tpmLang') || 'fr'; } catch (e) { return 'fr'; }
  });
  const setLanguage = (l) => {
    setLang(l);
    try { localStorage.setItem('tpmLang', l); } catch (e) {}
  };
  return (
    <LangContext.Provider value={{ lang, setLang: setLanguage, t: translations[l] }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  return useContext(LangContext);
}

export { translations };
